package com.forsys.revmantra.model;

public class SfqiExceptions {
	private String sfqiRefId;
	
	private String exceptionMsg;

	public SfqiExceptions(String sfqiRefId, String exceptionMsg) {
		this.sfqiRefId = sfqiRefId;
		this.exceptionMsg = exceptionMsg;
	}

	public String getSfqiRefId() {
		return sfqiRefId;
	}

	public void setSfqiRefId(String sfqiRefId) {
		this.sfqiRefId = sfqiRefId;
	}

	public String getExceptionMsg() {
		return exceptionMsg;
	}

	public void setExceptionMsg(String exceptionMsg) {
		this.exceptionMsg = exceptionMsg;
	}
	
	
}
