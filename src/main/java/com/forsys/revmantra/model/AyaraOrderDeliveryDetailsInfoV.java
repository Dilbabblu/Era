package com.forsys.revmantra.model;

import java.math.BigDecimal;
import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/*
 * AYAR-1787 By Chandra on Dec 30th 2021
 */
@Entity
@Table(name="AYARA_ORDER_DELIVERY_DETAILS_INFO_V")
public class AyaraOrderDeliveryDetailsInfoV {
	
	@Id
	@Column(name="DELIVERED_ID")
	private BigDecimal deliveredId;
	
	@Column(name="SRC_DELIVERED_LINE_ID")
	private String srcDeliveredLineId;
	
	@Column(name="DELIVERED_PERCENT")
	private BigDecimal deliveredPercent;
	
	@Column(name="DEL_AMOUNT")
	private BigDecimal delAmount;
	
	@Column(name="DELIVERED_TYPE")
	private String deliveredType;
	
	@Column(name="DEAL_LINE_ID")
	private BigDecimal dealLineId;
	
	@Column(name="SOURCE_LINE_ID")
	private BigDecimal sourceLineId;
	
	@Column(name="SOURCE_LINE_NUMBER")
	private String sourceLineNumber;
	
	@Column(name="PRODUCT_NAME")
	private String productName;
	
	@Column(name="DELIVERED_QUANTITY")
	private BigDecimal deliveredQuantity;
	
	@Column(name="DELIVERED_AMOUNT")
	private BigDecimal deliveredAmount;
	
	@Column(name="DELIVERED_DATE")
	private Timestamp deliveredDate;
	
	@Column(name="BOOKED_DATE")
	private Timestamp bookedDate;
	
	@Column(name="REVREC_START_DATE")
	private Timestamp revrecStartDate;
	
	@Column(name="REVREC_END_DATE")
	private Timestamp revrecEndDate;
	
	@Column(name="DELIVERED_STATUS")
	private String deliveredStatus;

	public BigDecimal getDealLineId() {
		return dealLineId;
	}

	public void setDealLineId(BigDecimal dealLineId) {
		this.dealLineId = dealLineId;
	}


	public BigDecimal getSourceLineId() {
		return sourceLineId;
	}

	public void setSourceLineId(BigDecimal sourceLineId) {
		this.sourceLineId = sourceLineId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public BigDecimal getDeliveredQuantity() {
		return deliveredQuantity;
	}

	public void setDeliveredQuantity(BigDecimal deliveredQuantity) {
		this.deliveredQuantity = deliveredQuantity;
	}

	public BigDecimal getDeliveredAmount() {
		return deliveredAmount;
	}

	public void setDeliveredAmount(BigDecimal deliveredAmount) {
		this.deliveredAmount = deliveredAmount;
	}

	public Timestamp getDeliveredDate() {
		return deliveredDate;
	}

	public void setDeliveredDate(Timestamp deliveredDate) {
		this.deliveredDate = deliveredDate;
	}

	public Timestamp getBookedDate() {
		return bookedDate;
	}

	public void setBookedDate(Timestamp bookedDate) {
		this.bookedDate = bookedDate;
	}

	public Timestamp getRevrecStartDate() {
		return revrecStartDate;
	}

	public void setRevrecStartDate(Timestamp revrecStartDate) {
		this.revrecStartDate = revrecStartDate;
	}

	public Timestamp getRevrecEndDate() {
		return revrecEndDate;
	}

	public void setRevrecEndDate(Timestamp revrecEndDate) {
		this.revrecEndDate = revrecEndDate;
	}

	public String getSourceLineNumber() {
		return sourceLineNumber;
	}

	public void setSourceLineNumber(String sourceLineNumber) {
		this.sourceLineNumber = sourceLineNumber;
	}

	public String getDeliveredStatus() {
		return deliveredStatus;
	}

	public void setDeliveredStatus(String deliveredStatus) {
		this.deliveredStatus = deliveredStatus;
	}

	public BigDecimal getDeliveredId() {
		return deliveredId;
	}

	public void setDeliveredId(BigDecimal deliveredId) {
		this.deliveredId = deliveredId;
	}

	public String getSrcDeliveredLineId() {
		return srcDeliveredLineId;
	}

	public void setSrcDeliveredLineId(String srcDeliveredLineId) {
		this.srcDeliveredLineId = srcDeliveredLineId;
	}

	public BigDecimal getDeliveredPercent() {
		return deliveredPercent;
	}

	public void setDeliveredPercent(BigDecimal deliveredPercent) {
		this.deliveredPercent = deliveredPercent;
	}

	public BigDecimal getDelAmount() {
		return delAmount;
	}

	public void setDelAmount(BigDecimal delAmount) {
		this.delAmount = delAmount;
	}

	public String getDeliveredType() {
		return deliveredType;
	}

	public void setDeliveredType(String deliveredType) {
		this.deliveredType = deliveredType;
	}	
	
	
	
}
